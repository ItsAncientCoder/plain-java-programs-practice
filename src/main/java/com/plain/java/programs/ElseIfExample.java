package com.plain.java.programs;

public class ElseIfExample {

	public static void main(String[] args) {
		int i = 10;

		if (i < 20) {
			System.out.println("<20");
		} else if (i < 15) {
			System.out.println("<15");
		} else {
			System.out.println("else");
		}
		System.out.println("end");
	}

}
